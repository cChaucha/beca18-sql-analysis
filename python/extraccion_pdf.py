def limpiar_seleccionados(df):

    # 🔥 1. Limpiar saltos de línea en TODO el DataFrame
    df = df.applymap(lambda x: x.replace("\n", " ").strip() if isinstance(x, str) else x)

    # 🔥 2. Normalizar espacios múltiples
    df = df.applymap(lambda x: " ".join(x.split()) if isinstance(x, str) else x)

    # DNI limpio
    df["DNI"] = df["DNI"].astype(str).str.strip()

    # textos
    df["MODALIDAD"] = df["MODALIDAD"].str.upper().str.strip()
    df["NOMBRES"] = df["NOMBRES"].str.strip()
    df["IES"] = df["IES"].str.strip()
    df["CARRERA"] = df["CARRERA"].str.strip()

    # 🔥 3. Normalizar CNA (versión robusta)
    df["MODALIDAD"] = df["MODALIDAD"].str.replace(
        r"BECA CNA Y POBLACIONES.*AFROPERUANAS",
        "BECA CNA Y PA",
        regex=True
    )

    # convertir puntajes
    df["CONCEPTO_A"] = pd.to_numeric(df["CONCEPTO_A"], errors="coerce")
    df["CONCEPTO_B"] = pd.to_numeric(df["CONCEPTO_B"], errors="coerce")
    df["PUNTAJE_FINAL"] = pd.to_numeric(df["PUNTAJE_FINAL"], errors="coerce")

    # eliminar basura
    df = df[df["DNI"].str.len() == 8]
    df = df.dropna()

    # 🔥 4. Convertir a ENTEROS
    df["CONCEPTO_A"] = df["CONCEPTO_A"].astype(int)
    df["CONCEPTO_B"] = df["CONCEPTO_B"].astype(int)
    df["PUNTAJE_FINAL"] = df["PUNTAJE_FINAL"].astype(int)

    return df

