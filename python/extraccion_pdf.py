def extraer_seleccionados(ruta_pdf):
    data = []

    with pdfplumber.open(ruta_pdf) as pdf:
        for i, page in enumerate(tqdm(pdf.pages)):
            try:
                table = page.extract_table()

                if table:
                    for row in table[1:]:

                        # Ignorar filas vacías
                        if not row or all(x is None for x in row):
                            continue

                        # 🔥 Caso normal (11 columnas)
                        if len(row) == 11:
                            data.append(row)

                        # 🔥 Caso con POBLACIÓN (12 columnas)
                        elif len(row) == 12:
                            # eliminamos la columna extra (posición 2 o 3)
                            nueva_fila = row[:2] + row[3:]
                            data.append(nueva_fila)

                        else:
                            print(f"⚠️ Fila irregular en página {i}: {row}")

            except Exception as e:
                print(f"❌ Error en página {i}: {e}")

    # Crear DataFrame
    df = pd.DataFrame(data)

    # Asignar columnas
    df.columns = [
        "N",
        "MODALIDAD",
        "DNI",
        "NOMBRES",
        "IES",
        "SEDE",
        "CARRERA",
        "CONCEPTO_A",
        "CONCEPTO_B",
        "PUNTAJE_FINAL",
        "CONDICION"
    ]

    return df

